const a = 7
let b = 3

// não esta adicionando e sim atribuindo mais valor
b += a // b = b + a
console.log(b)

// não esta subtraindo e sim atribuindo menos valor
b -= 4 // b = b - 4
console.log(b)

// não esta multiplicando e sim atribuindo mais valor
b *= 2 // b = b * 2
console.log(b)

// não esta dividindo e sim atribuindo menos valor
b /= 2 // b = b / 2
console.log(b)

// resto da divisão
b %= 2 // b = b % 2
console.log(b)